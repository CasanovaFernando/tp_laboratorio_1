
void suma (int a, int b)
    {
        int SUMA;
        SUMA= a+b;
        printf("\nEl resultado de la Multiplicacion es:  %d\n",SUMA);
    }

void multiplicacion (int a, int b)
    {
        int MULTIPLICACION;
        MULTIPLICACION= a*b;
        printf("\nEl resultado de la Multiplicacion es:  %d\n",MULTIPLICACION);
    }

void resta (int a, int b)
    {
        int RESTA;
        RESTA= a-b;
        printf("\nEl resultado de la Resta es:  %d\n",RESTA);
    }

void division (int a, int b)
    {
        int DIVISION;
        DIVISION= a/b;
        printf("\nEl resultado de la Division es:  %d\n",DIVISION);
    }

void factorial(int x)
 {
    int i;
    int fac = 1;
    for (i=2; i<=x; i=i+1)
    fac=fac*i;
    printf ("El factorial de %d es %d", x, fac);
 }













